"""Конфигурация логгера для отслеживания работы программы.

Модуль предназначен для инициализации системы логирования: настройки базового
корневого (root) логгера и создания дочернего логгера для изолированной
записи действий текущего модуля в файл.
Выходной путь файла логов строится относительно корня программы.
"""

import logging
from pathlib import Path

# Создание директории для лог-файлов. Если промежуточные папки отсутствуют,
# они будут созданы автоматически; если папка уже есть - ошибка не возникнет.
log_dir = Path('output/logs')
log_dir.mkdir(parents=True, exist_ok=True)

# Создание выходного файла логов
log_file = log_dir / 'log.log'

# Конфигурация базового рут-логгера
logging.basicConfig(level=logging.WARNING,
                    filename=str(log_file),
                    filemode="w",
                    format='[%(asctime)s] - %(levelname)s - %(message)s',
                    datefmt='%d-%b-%y %H:%M:%S',
                    encoding="utf-8")

# Создание дочернего логгера для непосредственной работы в программе
logger = logging.getLogger(__name__)
